import Foundation

/// Design tokens for border width values used across components.
///
/// These border width values provide consistent stroke widths for outlined elements.
///
public extension CGFloat {
  /// Border width for secondary buttons and outlined components.
  /// Value: 1.5
  static let borderWidthSecondary = 1.5

  /// Border width for primary text input components.
  /// Value: 2.0
  static let borderWidthPrimary = 2.0
}
